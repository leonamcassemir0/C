/*
    Tipos de variavéis:
        1) char (caractere) ocupa 1 byte, ou seja, 8 bits.
        2) int (inteiro) ocupa 2 bytes
            - Vai de -32.768 até +32767
        3) float (flutuante) ocupa 4 bytes
        4) void (vazio) não ocupa espaço.
        5) double é usado para números com mais casas decimais, afim de ter mais precisão.
            - Ocupa 8 bytes.

*/